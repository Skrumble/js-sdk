/**
 * @global
 * @summary
 * Removes any trailing slash from a string. If there is no trailing slash,
 * the original string is returned.
 *
 * @param {String} str The string to to remove a slash from
 * @returns {String} The original string without a trailing slash
 */
export function removeTrailingSlash(str) {

    if (typeof str !== "string") return false;

    return (str[str.length - 1] == "/")
        ? str.substr(0, str.length - 1)
        : str

}


/**
 * @summary
 * Removes `APISocket.current_user` from an array.
 *
 */
export function removeSelf(arr) {

    if (!arr || typeof arr !== "object" || arr.constructor.name !== "Array") return false;
    if (!APISocket.current_user) return arr;

    var return_array = [];

    arr.map((user) => {
        if ((user.id && user.id === APISocket.current_user.id) || !user.id) {
            return_array.push(user);
        }
    });

    return return_array;

}

